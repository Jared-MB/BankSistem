    private void setValues(){
        Empleado empleado = EmployeesUtils.GetEmployeeByID(Employees.ID);
        if (empleado instanceof Asesor asesor) {
            type.setText("Asesor");
            sueldoField.setText(asesor.getSueldo().toString());
        }
        else if (empleado instanceof Cajero cajero){
            type.setText("Cliente");
            sueldoField.setText(cajero.getSueldo().toString());
        }
        else if (empleado instanceof Gerente gerente) {
            type.setText("Gerente");
            sueldoField.setText(gerente.getSueldo().toString());
        } 
        if (empleado != null){
            nombreField.setText(empleado.getNombre());
            direccionField.setText(empleado.getDireccion());
            telefonoField.setText(empleado.getTelefono());
        }
        
    }
