    private void agregarEmpleadoFormBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregarEmpleadoFormBtnMouseClicked
        // TODO add your handling code here:
        Float sueldoValue;
        if (!"".equals(telefonoField.getText()) || !"".equals(direccionField.getText()) || !"".equals(sueldoField.getText())){
            try {
                Empleado employee = EmployeesUtils.GetEmployeeByID(Employees.ID);
                sueldoValue = Float.parseFloat(sueldoField.getText());
                Empleado empleado = null;
                switch (type.getText()) {
                case "Asesor" -> {
                    empleado = new Asesor(employee.getID(), employee.getNombre(), employee.getRFC(), telefonoField.getText(), direccionField.getText(), sueldoValue);
                    }
                case "Cajero" -> {
                    empleado = new Cajero(employee.getID(), employee.getNombre(), employee.getRFC(), telefonoField.getText(), direccionField.getText(), sueldoValue, 8);
                    }
                case "Gerente" -> {
                    empleado = new Gerente(employee.getID(), employee.getNombre(), employee.getRFC(), telefonoField.getText(), direccionField.getText(), sueldoValue);
                    }
                default -> throw new AssertionError();
                }
                EmployeesUtils.UpdateEmployee(empleado);
                this.dispose();
            }
            catch (IOException | NumberFormatException e){
                JOptionPane.showMessageDialog(null, "El sueldo no es un valor aceptable");
            }
        }
        else JOptionPane.showMessageDialog(null, "No todos los campos estan completos");
    }//GEN-LAST:event_agregarEmpleadoFormBtnMouseClicked

